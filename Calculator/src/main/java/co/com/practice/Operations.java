package co.com.practice;

public class Operations {

    public int multiplication(int a, int b){
        int c = a*b;
        return c;
    }

    public int division(int a, int b) {
        int c = a / b;
        return c;
    }

    public int sum(int a, int b){
        int c = a+b;
        return c;
    }

    public int subtract(int a, int b){
        int c = a-b;
        return c;
    }
}
